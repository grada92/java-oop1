public class Conto {
	
	int numeroConto;
	String nomeProprietario;
	float Saldo = 0;
	
	
	
	public Conto(int numeroConto, String nomeProprietario, float saldo) {
		super();
		this.numeroConto = numeroConto;
		this.nomeProprietario = nomeProprietario;
		Saldo = saldo;
	}
	

	
}
